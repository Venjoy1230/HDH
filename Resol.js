function resol() {
    document.getElementById("encours").style.display = "none";
    document.getElementById("resol").style.display = "block";
    document.getElementById("attente").style.display = "none";
    document.getElementById('id01').style.display='none';
};

function encours() {
    document.getElementById("encours").style.display = "block";
    document.getElementById("resol").style.display = "none";
    document.getElementById("attente").style.display = "none";
    document.getElementById('id01').style.display='none';
};